  id integer primary key,
  seder text not null,
  name text not null,
  chapters integer not null,
  claimed_by text,
  claimed_at timestamptz
);

-- The app talks to the database only through server-side API routes using the
-- service role key, which bypasses RLS. Enabling RLS with no policies blocks
-- any direct access with the public anon key.
alter table tractates enable row level security;

